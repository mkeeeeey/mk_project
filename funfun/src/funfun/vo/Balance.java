package funfun.vo;

public class Balance {
	private String memEmail;
	private String balType;
	private String balHis;
	private String balAmount;
	private String balDate;
	private String memName;
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	public String getMemEmail() {
		return memEmail;
	}
	public void setMemEmail(String memEmail) {
		this.memEmail = memEmail;
	}
	public String getBalType() {
		return balType;
	}
	public void setBalType(String balType) {
		this.balType = balType;
	}
	public String getBalHis() {
		return balHis;
	}
	public void setBalHis(String balHis) {
		this.balHis = balHis;
	}
	public String getBalAmount() {
		return balAmount;
	}
	public void setBalAmount(String balAmount) {
		this.balAmount = balAmount;
	}
	public String getBalDate() {
		return balDate;
	}
	public void setBalDate(String balDate) {
		this.balDate = balDate;
	}
}
