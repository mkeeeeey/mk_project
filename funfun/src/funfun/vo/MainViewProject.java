package funfun.vo;

public class MainViewProject {
	
	int code;
	String title;
	String category;
	int percent;
	int targetFunding;
	int currentFunding;
	double restDay;
	String image;

	
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getPercent() {
		return percent;
	}
	public void setPercent(int percent) {
		this.percent = percent;
	}
	public int getTargetFunding() {
		return targetFunding;
	}
	public void setTargetFunding(int targetFunding) {
		this.targetFunding = targetFunding;
	}
	public int getCurrentFunding() {
		return currentFunding;
	}
	public void setCurrentFunding(int currentFunding) {
		this.currentFunding = currentFunding;
	}
	public double getRestDay() {
		return restDay;
	}
	public void setRestDay(double restDay) {
		this.restDay = restDay;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
	
	
	
}
